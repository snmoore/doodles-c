// Reverse bits using a compact lookup table.
//  1. Reverse the lower nibble
//  2. Reverse the upper nibble
//  3. Swap the two reversed nibbles
uint8_t compact(uint8_t value) {
    static const uint8_t reverse[] = {
        0x0,  // 0000 -> 0000
        0x8,  // 0001 -> 1000
        0x4,  // 0010 -> 0100
        0xC,  // 0011 -> 1100
        0x2,  // 0100 -> 0010
        0xA,  // 0101 -> 1010
        0x6,  // 0110 -> 0110
        0xE,  // 0111 -> 1110
        0x1,  // 1000 -> 0001
        0x9,  // 1001 -> 1001
        0x5,  // 1010 -> 0101
        0xD,  // 1011 -> 1101
        0x3,  // 1100 -> 0011
        0xB,  // 1101 -> 1011
        0x7,  // 1110 -> 0111
        0xF,  // 1111 -> 1111
    };

    // uint8_t lower = reverse[value & 0xF];
    // uint8_t upper = reverse[value >> 4];
    // return lower << 4 | upper;
    return reverse[value & 0xF] << 4 | reverse[value >> 4];
}

// Reverse the bits in a byte by successive swapping.
//  1. Swap the upper and lower nibbles  (sets of 4 bits)   ABCDEFGH -> EFGHABCD
//  2. Swap adjacent pairs of bits       (sets of 2 bits)   EFGHABCD -> GHEFCDAB
//  3. Swap adjacent bits                (individual bits)  GHEFCDAB -> HGFEDCBA
uint8_t swapping(uint8_t value) {
    value = (value & 0xF0) >> 4 | (value & 0x0F) << 4;  // 1111 0000 >> 4 | 0000 1111 << 4
    value = (value & 0xCC) >> 2 | (value & 0x33) << 2;  // 1100 1100 >> 2 | 0011 0011 << 2
    value = (value & 0xAA) >> 1 | (value & 0x55) << 1;  // 1010 1010 >> 1 | 0101 0101 << 1
    return value;
}

