// Reverse the bits in a byte.
//
// E.g. 00001010 -> 01010000

#include <limits.h>     // For CHAR_BIT
#include <stddef.h>     // For size_t, sizeof
#include <stdint.h>     // For uint8_t
#include <stdio.h>      // For printf
#include <stdlib.h>     // For EXIT_SUCCESS

#define NELEMENTS(a)    (sizeof(a) / sizeof(a[0]))

// Reverse bits in a byte using the obvious method.
// 8 iterations per byte.
uint8_t obvious(uint8_t value) {
    uint8_t reverse = 0;
    size_t bits = sizeof(value) * CHAR_BIT;
    for(size_t i = 0; i < bits; i++) {
        reverse <<= 1;
        reverse |= value & 1;
        value   >>= 1;
    }
    return reverse;
}

// Reverse bits in a byte using the obvious method, but shortcut when there are no bits remaining.
// Up to 8 iterations per byte.
uint8_t shortcut(uint8_t value) {
    uint8_t reverse = 0;
    size_t bits = sizeof(value) * CHAR_BIT;
    for( ; value > 0; value >>= 1) {
        reverse <<= 1;
        reverse |= value & 1;
        bits--;
    }
    return reverse << bits;
}

